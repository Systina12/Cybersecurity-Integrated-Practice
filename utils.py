def rl(ip,port):
    sock = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
    sock.connect((ip, port))
    def recv_loop(sock):
        while True:
            try:
                data = sock.recv(4096)
                if data:
                    print(data.decode("gbk", errors="ignore"), end="")
                else:
                    time.sleep(0.05)
            except:
                time.sleep(0.05)

    t = threading.Thread(target=recv_loop, args=(sock,))
    t.daemon = True
    t.start()

def interactive_shell(ip, port):
    sock = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
    sock.connect((ip, port))

    def recv_loop(sock):
        while True:
            try:
                data = sock.recv(4096)
                if not data:
                    print("\n[!] Connection closed by remote")
                    break
                print(data.decode("gbk", errors="ignore"), end="", flush=True)
            except:
                time.sleep(0.05)

    t = threading.Thread(target=recv_loop, args=(sock,))
    t.daemon = True
    t.start()

    try:
        while True:
            cmd = input()
            if cmd.strip().lower() in ("exit", "quit"):
                break
            sock.sendall((cmd + "\n").encode("gbk", errors="ignore"))
    except (KeyboardInterrupt, EOFError):
        pass
    finally:
        sock.close()
        print("\n[*] Shell closed")


